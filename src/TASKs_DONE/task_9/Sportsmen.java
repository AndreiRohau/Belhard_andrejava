public class Sportsmen {
    private int g;
    private int s;
    private int b;

    public Sportsmen (int g, int s, int b) {
        this.g = g;
        this.s = s;
        this.b = b;
    }

    public int getG() {
        return g;
    }

    public void setG(int g) {
        this.g = g;
    }

    public int getS() {
        return s;
    }

    public void setS(int s) {
        this.s = s;
    }

    public int getB() {
        return b;
    }

    public void setB(int b) {
        this.b = b;
    }
}
